public class for_while {
    public static void main(String[] args) {
        int n = 5;
        for (int i=1; i<=n; i++) {
            System.out.println(i);
        }

        int m = 5;
        int i = 0; 
        while (i<m) {
            System.out.println(i+1);
            i++;
        }
    }
}
